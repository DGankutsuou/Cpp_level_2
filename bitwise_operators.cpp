#include <iostream>
using namespace std;

int	main(void)
{
	cout << "Bitwise and operator 12 | 25 = " << (12 & 25) << endl;
	cout << "Logical and operator 12 | 25 = " << (12 && 25) << endl;
	cout << "Bitwise or operator 12 | 25 = " << (12 | 25) << endl;
	cout << "Logical or operator 12 | 25 = " << (12 || 25) << endl;
}
